# ---------------------------------------------------------------------------
# WorkflowTrace
# ---------------------------------------------------------------------------


@dataclass
class WorkflowTrace:
    """Aggregated trace of an entire workflow run."""

    trace_id: str = field(default_factory=lambda: _uid("trace_"))
    tool_traces: list[ToolCallTrace] = field(default_factory=list)
    final_decision: str = ""
    total_duration_ms: float = 0.0
    status: str = "running"
    metadata: dict[str, Any] = field(default_factory=dict)

    def __post_init__(self) -> None:
        if not self.total_duration_ms and self.tool_traces:
            self.total_duration_ms = sum(
                t.duration_ms for t in self.tool_traces
            )

    def to_dict(self) -> dict[str, Any]:
        return {
            "trace_id": self.trace_id,
            "tool_traces": [t.to_dict() for t in self.tool_traces],
            "final_decision": self.final_decision,
            "total_duration_ms": self.total_duration_ms,
            "status": self.status,
            "metadata": dict(self.metadata),
        }

    @classmethod
    def from_dict(cls, d: dict[str, Any]) -> WorkflowTrace:
        traces = [
            ToolCallTrace.from_dict(t)
            for t in d.get("tool_traces", [])
        ]
        return cls(
            trace_id=str(d.get("trace_id", _uid("trace_"))),
            tool_traces=traces,
            final_decision=str(d.get("final_decision", "")),
            total_duration_ms=float(d.get("total_duration_ms", 0.0)),
            status=str(d.get("status", "running")),
            metadata=dict(d.get("metadata", {})),
        )
