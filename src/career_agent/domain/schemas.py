"""Industrial-grade domain schemas for the Agentic RAG system.

All scores are validated via ``validate_score`` — bool, NaN, inf, and
out-of-range values are rejected at construction time.

Every schema supports ``to_dict()`` / ``from_dict()`` for serialisation.
"""

from __future__ import annotations

import uuid
from dataclasses import dataclass, field
from typing import Any

from career_agent.domain.validation import validate_score

# ---------------------------------------------------------------------------
# helpers
# ---------------------------------------------------------------------------


def _uid(prefix: str = "") -> str:
    return f"{prefix}{uuid.uuid4().hex[:12]}"


# ---------------------------------------------------------------------------
# ParsedJD
# ---------------------------------------------------------------------------


@dataclass
class ParsedJD:
    """Structured representation of a parsed job description."""

    job_title: str = ""
    job_direction: str = "general"
    hard_skills: list[str] = field(default_factory=list)
    bonus_skills: list[str] = field(default_factory=list)
    soft_skills: list[str] = field(default_factory=list)
    keywords: list[str] = field(default_factory=list)
    raw_text: str = ""
    metadata: dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> dict[str, Any]:
        return {
            "job_title": self.job_title,
            "job_direction": self.job_direction,
            "hard_skills": list(self.hard_skills),
            "bonus_skills": list(self.bonus_skills),
            "soft_skills": list(self.soft_skills),
            "keywords": list(self.keywords),
            "raw_text": self.raw_text,
            "metadata": dict(self.metadata),
        }

    @classmethod
    def from_dict(cls, d: dict[str, Any]) -> ParsedJD:
        return cls(
            job_title=str(d.get("job_title", "")),
            job_direction=str(d.get("job_direction", "general")),
            hard_skills=list(d.get("hard_skills", [])),
            bonus_skills=list(d.get("bonus_skills", [])),
            soft_skills=list(d.get("soft_skills", [])),
            keywords=list(d.get("keywords", [])),
            raw_text=str(d.get("raw_text", "")),
            metadata=dict(d.get("metadata", {})),
        )


