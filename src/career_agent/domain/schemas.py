_DECISION_NEXT_ACTION: dict[str, str] = {
    "continue": "analyze_match",
    "retry": "rewrite_query",
    "fallback": "end",
}


@dataclass
class AgentDecision:
    """A structured decision made by the agent planner."""

    decision: str = "continue"
    reason: str = ""
    next_action: str = ""
    metadata: dict[str, Any] = field(default_factory=dict)

    def __post_init__(self) -> None:
        if not self.next_action:
            self.next_action = _DECISION_NEXT_ACTION.get(
                self.decision, "analyze_match"
            )

    def to_dict(self) -> dict[str, Any]:
        return {
            "decision": self.decision,
            "reason": self.reason,
            "next_action": self.next_action,
            "metadata": dict(self.metadata),
        }

    @classmethod
    def from_dict(cls, d: dict[str, Any]) -> AgentDecision:
        return cls(
            decision=str(d.get("decision", "continue")),
            reason=str(d.get("reason", "")),
            next_action=str(d.get("next_action", "")),
            metadata=dict(d.get("metadata", {})),
        )


